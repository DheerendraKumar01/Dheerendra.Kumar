package com.datatypes;

public class DatatTypesDemo {
	
	public static void main(String args[]) {
		
		int length = 5; //local variable for this method
		System.out.println("length  variable " + length);
		
		double d = 12.5;
		System.out.println("Double  variable " + d);
		
		String dummyvariable = "ram";
		System.out.println("String  variable " + dummyvariable);
		
		float f = (float) 15.5; //Type casting
		System.out.println("Float  variable " + f);
		
		char c = 'a';
		System.out.println("Char  variable " + c);
		
		long l = 1235;
		System.out.println("Long  variable " + l);
		
		byte b = 3;
		System.out.println("Byte  variable " + b);
		
	}
	
}
