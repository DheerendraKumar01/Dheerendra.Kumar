package com.demo;

public class AdditionDemo {

	public static void main(String args[]) {
		
		int numberone = 5;
		int numbertwo = 7;
		int numberthree = 9;
		int numberfour = 6;
		
		int temp = numberone + numbertwo;
		int temp1 = numberthree - numberfour;
		
		System.out.println("Addition of two numbers: " + temp);
		System.out.println("Sub of two numbers: " + temp1);
	}
	
}
